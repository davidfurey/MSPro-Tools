                $dict->{Height}=PDFNum($h);
                $dict->{ColorSpace}=PDFName('DeviceGray');
                $dict->{Filter}=PDFArray(PDFName('FlateDecode'));
                $dict->{BitsPerComponent}=PDFNum($bpc);
                $self->{SMask}=$dict;
