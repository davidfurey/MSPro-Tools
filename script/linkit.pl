            my $scanline=1+ceil($bpc*4*$w/8);
            my $bpp=ceil($bpc*4/8);
            my $clearstream=unprocess($bpc,$bpp,4,$w,$h,$scanline,\$self->{' stream'});
            delete $self->{' nofilt'};
            delete $self->{' stream'};
            foreach my $n (0..($h*$w)-1) {
