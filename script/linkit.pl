sub app_options {
    my $help = 0;		# handled locally
    my $ident = 0;		# handled locally
    my $man = 0;		# handled locally

    my $pod2usage = sub {
        # Load Pod::Usage only if needed.
        require Pod::Usage;
        Pod::Usage->import;
        &pod2usage;
    };

    if ( !GetOptions(
		     'pdf=s'		=> \$outpdf,
		     'xpos=i'		=> \$xpos,
		     'ypos=i'		=> \$ypos,
		     'iconsize=i'	=> \$iconsz,
		     'padding=i'	=> \$padding,
		     'vertical'		=> \$vertical,
		     'border'		=> \$border,
		     'ident'		=> \$ident,
		     'verbose|v+'	=> \$verbose,
		     'trace'		=> \$trace,
		     'help|?'		=> \$help,
		     'man'		=> \$man,
		     'debug'		=> \$debug,
		    ) or $help )
    {
	$pod2usage->(2);
    }
    if ( $man or $help ) {
	$pod2usage->(1) if $help;
	$pod2usage->(VERBOSE => 2) if $man;
    }
    app_ident() if $ident;
    $pod2usage->(1) if @ARGV < 1 || @ARGV > 2;
}

